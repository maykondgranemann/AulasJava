package modulo1;

public class a013 {
    public static void main(String[] args) {

        for(int linha = 1; linha <= 10; linha ++){
            System.out.printf("%d - ",linha);

            for(int colunas = 1; colunas <= 10; colunas ++){
                System.out.printf("%d ",colunas);
            }
            System.out.println();
        }
    }
}
