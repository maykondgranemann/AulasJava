package modulo1;

public class s14 {
   public static void main(String[] args) {
      for (int i = 0; i < 10; i++) {          
         System.out.println(i);
      }

      for(int i = 10; i > 0; i--){
         System.out.println(i);
      }  
      
      for (int i = 0; i < 10; i++) {
         System.out.println("Maykon");
      }
   } 
}
