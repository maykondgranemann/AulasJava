package modulo1;

public class a009 {
    public static void main(String[] args) {
        
        int repeticoes = 1;
        while(repeticoes <= 5){            
            System.out.printf("\nrepetição %d", repeticoes);
            repeticoes = repeticoes + 1;
        }
        repeticoes = 1;
        while(repeticoes <= 5){            
            System.out.printf("\nrepetição %d", repeticoes);
            repeticoes += 1;
        }
        repeticoes = 1;
        while(repeticoes <= 5){            
            System.out.printf("\nrepetição %d", repeticoes);
            repeticoes ++;
        }
    }
}
