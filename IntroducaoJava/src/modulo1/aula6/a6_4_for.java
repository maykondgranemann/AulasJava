package modulo1.aula6;

public class a6_4_for {
    public static void main(String[] args) {
        int contador = 0;
        while(contador <10){
            System.out.println(contador);
            contador++;
        }

        for(int cont = 0; cont < 10; cont++){
            System.out.println(cont);
        }
    }
}
